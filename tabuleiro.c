/* ================================= tabuleiro.c ====================================== */
#include <stdio.h>
#include <stdlib.h>
void imprimeTabuleiro(){
  int i = 0;
  int j = 0;
  printf("\033[0;0f");
  printf("|  A   |  B   |  C   |  D   |  E   |  F   |  G   |  H   |  I   |  J   |\n");
  printf("|------|------|------|------|------|------|------|------|------|------|--\n");
  for (i = 0; i < 10; i++){
    for (j = 0; j < 2; j++){
      if (j % 2 == 1){
        printf("|      |      |      |      |      |      |      |      |      |      |\n");
      } 
      else {
        printf("|      |      |      |      |      |      |      |      |      |      | %i\n", i+1);
      }
    }
    if (i < 9)
      printf("|------+------+------+------+------+------+------+------+------+------|--\n");
    else
      printf("|---------------------------------------------------------------------|\n");
  }
}

